package com.example.projectbd;

public class Metode {
    private String metode;
    private String nama;

    public Metode(String metode, String nama) {
        this.metode = metode;
        this.nama = nama;
    }

    public String getMetode() {
        return metode;
    }

    public void setMetode(String metode) {
        this.metode = metode;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

}

